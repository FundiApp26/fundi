let currentLang = localStorage.getItem('fundi-lang') || 'fr';

function setLanguage(lang) {
  currentLang = lang;
  localStorage.setItem('fundi-lang', lang);
  document.documentElement.setAttribute('data-lang', lang);

  // Update all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });

  // Update placeholders
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (translations[lang][key]) {
      el.placeholder = translations[lang][key];
    }
  });

  // Update language switcher
  document.querySelectorAll('[data-lang-switch]').forEach(el => {
    el.classList.toggle('font-bold', el.getAttribute('data-lang-switch') === lang);
    el.classList.toggle('text-fundi-orange', el.getAttribute('data-lang-switch') === lang);
  });
}

function toggleLanguage() {
  setLanguage(currentLang === 'fr' ? 'en' : 'fr');
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  setLanguage(currentLang);
});
